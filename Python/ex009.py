a = 30
b = 20
c = True
d = False

print('1-',a + b)  #Soma entre 'a' e 'b'.
print('2-',a - b)  #Subtração entre 'a' e 'b'.
print('3-',a * b)  #Multiplicação entre 'a' e 'b'.
print('4-',a / b)  #Divide 'a' por 'b'.
print('5-',a // b) #Pega apenas a parte intera da fração.
print('6-',a ** b) #O 'a' está elevado a potência de 'b'.
print('7-',c & d)  #True se 'd' também for True (Função lógica "e").
print('8-',c | d)  #True caso 'c' ou 'd' forem True (Função lógica "ou").
print('8-',c ^ d)  #True caso 'c' ou 'd' forem True, nunca ambos!(Função lógica "ou...ou").
print('9-',c == d) #True caso 'c' for igual a 'd'.
print('10-',c != d)#True caso 'c' for diferente de 'd'.
print('11-',a < b, a <= b) #True caso 'a' é menor que (menor ou igual) 'b'.
print('12-',a > b, a >= b) #True caso 'a' é maior que (maior ou igual) 'b'.
print('13-',a is b)#True se 'a' é igual a 'b'(Duas variáveis se referem ao mesmo objeto).
print('14-',a is not b)#True se 'a' não é igual a 'b'(Duas variáveis não se referem ao mesmo objeto).
